package operators;

public class Arithmetic {

	public static void main(String[] args) {
		int a=10;
		int b=20;
		System.out.println(a+b);
		System.out.println(a%b);//10--->reaminder
		System.out.println(a/b);//0---->quotient
		System.out.println(10.0/2.0);//5.0;
		System.out.println(10.0/2);//5;
		float f=10.0f;
		System.out.println(f);
		double d=20.0;    //no need to mention 'd' at end
		System.out.println(d);

		

	}
}
